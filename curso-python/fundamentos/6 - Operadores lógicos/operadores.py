# tabela verdade do and

print(True and True)
print(True and False) # Se tiver ao menos um falso na expressao retorna falso
print(False and True)

print(False and False)


# tabela verdade do Or

print(True or True)
print(True or False)
print(False or True) # Se tiver ao menos um true na expressão retorna true
print(False or False)

# Tabela verdade do NOR (Ou um ou outro)

print('True != True')
print(True != True)

print('True != False')
print(True != False)

print('False != True')
print(False != True)

print('False != False')
print(False != False)


# Operação de negação 
print (not True) # Negando uma expressão True
print (not False) # Negando uma expressão False

print(not 0)